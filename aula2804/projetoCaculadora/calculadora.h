/**
 *
 * @param a Valor inteiro passado como argumento
 * @param b Valor inteiro passado como argumento
 * @return Devolve o valor da soma dos dois inteiros passados como arumento
 */
int somar(int a, int b);


/**
 *
 * @param a Valor inteiro passado como argumento
 * @param b Valor inteiro passado como argumento
 * @return Devolve o valor da subtracao dos dois inteiros passados como argumento
 */
int subtrair(int a, int b);


/**
 *
 * @param a Valor inteiro passado como argumento
 * @param b Valor inteiro passado como argumento
 * @return Devolve o valor da multiplicacao dos dois inteiros passados como argumento
 */
int multiplicar(int a, int b);


/**
 *
 * @param a Valor inteiro passado como argumento
 * @param b Valor inteiro passado como argumento
 * @return Devolve o valor da divisao do primeiro inteiro pelo segundo inteiro passado como argumento
 * @note Certifique-se de que 'b' não é zero para evitar divisão por zero.
 */
int dividir(int a, int b);