/* Função somar: recebe dois valores inteiros e devole como
    resultado sua soma */
int somar(int a, int b);

/* Função subtrair: recebe dois valores inteiros e devole como
    resultado a subtração do primeiro valor pelo segundo valor */
int subtrair(int a, int b);

/* Função multiplicar: recebe dois valores inteiros e devole como
    resultado a multiplicação do primeiro valor pelo segundo valor */
int multiplicar(int a, int b);

/* Função dividir: recebe dois valores inteiros e devole como
    resultado a divusão do primeiro valor pelo segundo valor.
    Não deve fazer divisão por zero */
int dividir(int a, int b);

/*
    Função calcular area da circunferencia. Recebe o raio e devolve
    a area.
*/
float calcularArea(float raio);

