#include <stdio.h>
//Exemplo de tipos de dados
int main() {
    printf("Temos 05 tipos básicos de dados: \n");
    printf("Numeros: int, float, double \n");
    printf("Caractere: char - um caracter \n");
    printf("Void - representa ausencia de dado \n");
    long int a = 2147483649;
    char l= 'B';
    double x = 30.89;
    printf("Valor de a: %ld \n", a);
    printf("Valor de l: %c \n", l);
    printf("Valor de x: %.2lf \n", x);
    return 0;
}
