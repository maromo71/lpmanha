#include <stdio.h>

int main(void)
{
    printf("Tamanho em Bytes de um char: %d bytes\n", sizeof(char));
    printf("Tamanho em Bytes de um int: %d bytes\n", sizeof(int));
    printf("Tamanho em Bytes de um float: %d bytes\n", sizeof(float));
    printf("Tamanho em Bytes de um double: %d bytes\n", sizeof(double));
    printf("Tamanho em Bytes de um long double %d bytes\n", sizeof(long double));
    return 0;
}